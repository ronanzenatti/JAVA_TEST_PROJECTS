    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        DateFormat dateEU = new SimpleDateFormat("yyyy-MM-dd");
        DateFormat dateBR = new SimpleDateFormat("dd/MM/yyyy");

        EntityManagerFactory objFactory = Persistence.createEntityManagerFactory("RT_ParkingPU");
        EntityManager manager = objFactory.createEntityManager();
        Clientes c = new Clientes();
        ClientesJpaController cJPA = new ClientesJpaController(objFactory);
        
        c.setNome(txtNome.getText());
        c.setTipoPessoa(cboTipoPessoa.getSelectedItem().toString().charAt(0));
        c.setDocFed(txtDocFed.getText());
        c.setDtNasc(txtDtNasc.getDate());
        c.setRua(txtRua.getText());
        c.setNumero(txtNumero.getText());
        c.setBairro(txtBairro.getText());
        c.setCidade(txtCidade.getText());
        c.setTelefones(txtTelefones.getText());
        c.setEmail(txtEmail.getText());        
        
        if (cJPA.create(c)) {
            JOptionPane.showMessageDialog(null, "Salvo com Sucesso!!!", "RT Parking", JOptionPane.INFORMATION_MESSAGE);
            ListaClientes lp = new ListaClientes();
            lp.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

