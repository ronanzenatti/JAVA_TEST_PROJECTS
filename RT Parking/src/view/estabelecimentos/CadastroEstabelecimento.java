    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        DateFormat dateEU = new SimpleDateFormat("yyyy-MM-dd");
        DateFormat dateBR = new SimpleDateFormat("dd/MM/yyyy");

        EntityManagerFactory objFactory = Persistence.createEntityManagerFactory("RT_ParkingPU");
        EntityManager manager = objFactory.createEntityManager();
        Estabelecimentos e = new Estabelecimentos();

        EstabelecimentosJpaController eJPA = new EstabelecimentosJpaController(objFactory);

        e.setNome(txtNome.getText());
        e.setCnpj(txtCNPJ.getText());
        e.setVagasCarro(Integer.valueOf((txtVC.getText().trim().isEmpty()) ? "0" : txtVC.getText()));
        e.setVagasMoto(Integer.valueOf((txtVM.getText().trim().isEmpty()) ? "0" : txtVM.getText()));
        e.setRua(txtRua.getText());
        e.setNumero(txtNumero.getText());
        e.setComplemento(txtComplemento.getText());
        e.setBairro(txtBairro.getText());
        e.setCidade(txtCidade.getText());
        e.setTelefones(txtTelefones.getText());
        e.setEmail(txtEmail.getText());
        e.setDtInicio(txtDataInicio.getDate());
        e.setDtFim(txtDataTermino.getDate());

        if (eJPA.create(e)) {
            JOptionPane.showMessageDialog(null, "Salvo com Sucesso!!!", "RT Parking", JOptionPane.INFORMATION_MESSAGE);
            ListaEstabelecimentos lp = new ListaEstabelecimentos();
            lp.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

