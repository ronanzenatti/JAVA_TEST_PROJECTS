    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (jTablePessoas.getSelectedRow() != -1 && jTablePessoas.getSelectedRowCount() == 1) {
            AlterarPessoa au = new AlterarPessoa((int) jTablePessoas.getValueAt(jTablePessoas.getSelectedRow(), 0));
            au.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um registro para alterar... \n"
                    + "Escolha apenas um único registro !!!", "Sistema Comercial", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (jTablePessoas.getSelectedRow() != -1 && jTablePessoas.getSelectedRowCount() == 1) {
            int option = JOptionPane.showConfirmDialog(null, "Deseja realmete excluir o registro ?", "Sistema Comercial", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (option == JOptionPane.YES_OPTION) {
                PessoasDAO uDAO = new PessoasDAO();

                if (uDAO.delete((int) jTablePessoas.getValueAt(jTablePessoas.getSelectedRow(), 0))) {
                    readJTable();
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um registro para alterar... \n"
                    + "Escolha apenas um único registro !!!", "Sistema Comercial", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

