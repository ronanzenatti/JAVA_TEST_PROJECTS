    public void readJTable() {
        String tipo;
        DefaultTableModel modelo = (DefaultTableModel) jTablePessoas.getModel();
        modelo.setNumRows(0);
        PessoasDAO pDAO = new PessoasDAO();

        for (Pessoas p : pDAO.read()) {
            if (p.getTipo() == 'F') {
                tipo = "Física";
            } else {
                tipo = "Jurídica";
            }
            modelo.addRow(new Object[]{
                p.getIdpessoa(),
                p.getNome(),
                tipo,
                p.getDoc_fed(),
                p.getCidade(),
                p.getEmail(),
                p.getTel_fixo(),
                p.getTel_cel()
            });
        }
    }

    public void readForFind() {
        String tipo;
        String busca = txtFind.getText();
        String campo[] = new String[8];
        DefaultTableModel modelo = (DefaultTableModel) jTablePessoas.getModel();
        modelo.setNumRows(0);
        PessoasDAO pDAO = new PessoasDAO();

        campo[0] = "idpessoa";
        campo[1] = "nome";
        campo[2] = "tipo";
        campo[3] = "doc_fed";
        campo[4] = "cidade";
        campo[5] = "email";
        campo[6] = "tel_fixo";
        campo[7] = "tel_cel";

        for (Pessoas p : pDAO.readForFind(campo[cboFind.getSelectedIndex()], busca)) {
            if (p.getTipo() == 'F') {
                tipo = "Física";
            } else {
                tipo = "Jurídica";
            }
            modelo.addRow(new Object[]{
                p.getIdpessoa(),
                p.getNome(),
                tipo,
                p.getDoc_fed(),
                p.getCidade(),
                p.getEmail(),
                p.getTel_fixo(),
                p.getTel_cel()
            });

        }
    }

