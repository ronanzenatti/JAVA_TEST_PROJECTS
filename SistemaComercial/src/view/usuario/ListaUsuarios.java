    public void readJTable() {
        String perfil;
        DefaultTableModel modelo = (DefaultTableModel) jTableUsuarios.getModel();
        modelo.setNumRows(0);
        UsuariosDAO uDAO = new UsuariosDAO();

        for (Usuarios u : uDAO.read()) {
            if (u.getPerfil() == 'A') {
                perfil = "Administrador";
            } else {
                perfil = "Usuário";
            }

            modelo.addRow(new Object[]{
                u.getIdusuario(),
                u.getNome(),
                u.getUsuario(),
                perfil
            });
        }
    }

    public void readForFind() {
        String perfil;
        String busca = txtFind.getText();
        String campo[] = new String[4];
        DefaultTableModel modelo = (DefaultTableModel) jTableUsuarios.getModel();
        modelo.setNumRows(0);
        UsuariosDAO uDAO = new UsuariosDAO();

        campo[0] = "idusuario";
        campo[1] = "nome";
        campo[2] = "usuario";
        campo[3] = "perfil";

        if (cboFind.getSelectedIndex() == 3) {
            busca = String.valueOf(txtFind.getText().charAt(0));
        }

        for (Usuarios u : uDAO.readForFind(campo[cboFind.getSelectedIndex()], busca)) {
            if (u.getPerfil() == 'A') {
                perfil = "Administrador";
            } else {
                perfil = "Usuário";
            }

            modelo.addRow(new Object[]{
                u.getIdusuario(),
                u.getNome(),
                u.getUsuario(),
                perfil
            });
        }
    }

