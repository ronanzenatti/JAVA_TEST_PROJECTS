    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (jTableUsuarios.getSelectedRow() != -1 && jTableUsuarios.getSelectedRowCount() == 1) {
            AlterarUsuario au = new AlterarUsuario(Integer.valueOf(jTableUsuarios.getValueAt(jTableUsuarios.getSelectedRow(), 0).toString()));
            au.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um registro para alterar... \n"
                    + "Escolha apenas um único registro !!!", "Sistema Comercial", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (jTableUsuarios.getSelectedRow() != -1 && jTableUsuarios.getSelectedRowCount() == 1) {
            int option = JOptionPane.showConfirmDialog(null, "Deseja realmete excluir o registro ?", "Sistema Comercial", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (option == JOptionPane.YES_OPTION) {
                UsuariosDAO uDAO = new UsuariosDAO();
                
                if (uDAO.delete((int) jTableUsuarios.getValueAt(jTableUsuarios.getSelectedRow(), 0))) {
                    readJTable();
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um registro para alterar... \n"
                    + "Escolha apenas um único registro !!!", "Sistema Comercial", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

