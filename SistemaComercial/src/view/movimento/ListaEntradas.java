    public void readJTable() throws ParseException {
        DefaultTableModel modelo = (DefaultTableModel) jTableEntradas.getModel();
        modelo.setNumRows(0);
        MovimentosDAO mDAO = new MovimentosDAO();

        for (Movimentos m : mDAO.read('E')) {
            modelo.addRow(new Object[]{
                m.getIdmovimento(),
                m.getData(),
                m.getNomePessoa(),
                m.getDesconto(),
                m.getVl_total(),                
            });
        }
    }
    
