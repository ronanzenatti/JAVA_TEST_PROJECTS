    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (jTableEntradas.getSelectedRow() != -1 && jTableEntradas.getSelectedRowCount() == 1) {
            AlterarProduto au = new AlterarProduto((int) jTableEntradas.getValueAt(jTableEntradas.getSelectedRow(), 0));
            au.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um registro para alterar... \n"
                + "Escolha apenas um único registro !!!", "Sistema Comercial", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (jTableEntradas.getSelectedRow() != -1 && jTableEntradas.getSelectedRowCount() == 1) {
            int option = JOptionPane.showConfirmDialog(null, "Deseja realmete excluir o registro ?", "Sistema Comercial", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (option == JOptionPane.YES_OPTION) {
                ProdutosDAO pDAO = new ProdutosDAO();

                if (pDAO.delete((int) jTableEntradas.getValueAt(jTableEntradas.getSelectedRow(), 0))) {
                    try {
                        readJTable();
                    } catch (ParseException ex) {
                        Logger.getLogger(ListaEntradas.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um registro para alterar... \n"
                + "Escolha apenas um único registro !!!", "Sistema Comercial", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

